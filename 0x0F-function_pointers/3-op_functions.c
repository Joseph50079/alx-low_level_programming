#include "3-calc.h"


/**
 * op_add - add int
 * @a: first int
 * @b: second int
 * Return: return compute value
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract int
 * @a: first int
 * @b: second int
 * Return: return compute value
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul int
 * @a: first int
 * @b: second int
 * Return: return compute value
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide int
 * @a: first int
 * @b: second int
 * Return: return compute value
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modules int
 * @a: first int
 * @b: second int
 * Return: return compute value
 */

int op_mod(int a, int b)
{
	return (a % b);
}
