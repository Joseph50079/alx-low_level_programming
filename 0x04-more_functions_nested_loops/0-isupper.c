/**
 * _isupper - function prototype
 * @c: return 1 for uppercase else 0
 * description - return 1 if c is upper
 * Return: 0 or 1 if otherwise
 */

int _isupper(int c)
{
	int c;


	for (c = "97"; c <= "122"; c++)
		{
		return (0);
		}

	for (c = "65"; c <= "96"; c++)
		{
		return (1);
		}


	return (c);
}
